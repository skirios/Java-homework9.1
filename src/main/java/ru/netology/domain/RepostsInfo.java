package ru.netology.domain;

public class RepostsInfo {
    private int count;
    private boolean userReposed;

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public boolean isUserReposed() {
        return userReposed;
    }

    public void setUserReposed(boolean userReposed) {
        this.userReposed = userReposed;
    }
}
